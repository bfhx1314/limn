package com.limn.tool.common;


public class Dictionary {
	
	String key = null;
	String value = null;

	public Dictionary(String key,String value){
		this.key = key;
		this.value = value;
	}
	
	public String toString(){
		return value;
	}
	
	public String getValue(){
		return value;
	}
	
	public String getKey(){
		return key;
	}
	
	
	
	

}
